import java.util.*;

public abstract class Area {
	//////////////////////////////////////////////////////////////////////////////////////////////////
	// globals
	//////////////////////////////////////////////////////////////////////////////////////////////////
	private String location;
	private ArrayList<NPC> people;
	private boolean playerhere;

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// default constructor
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public Area(String locale, int NPCcount) {
		location = locale;
		people = new ArrayList<NPC>(0);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// returns name of area
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public String getLocation() {
		return location;
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// change name of area in case needed.
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public void setLocation(String locale) {
		location = locale;
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// constructs NPCs, room allowed for different types later.
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public void setNPC(int i, String type, String N, int A) {
		if (type.equals("knight"))
			people.add(new Knight(N, A, location));
		else if (type.equals("villager"))
			people.add(new Villager(N, A));
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// To string
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public String toString() {
		if (playerhere)
			return "@";
		if (location.equals("town"))
			return "#";
		else if (location.equals("forest"))
			return ".";
		else
			return " ";
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// returns the array of npcs for an area
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public NPC[] getNPCArray() {
		return people.toArray(new NPC[people.size()]);
	}
}