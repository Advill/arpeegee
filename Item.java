public class Item
{
	//////////////////////////////////////////////////////////////////////////////////////////////////
	//attributes
	//////////////////////////////////////////////////////////////////////////////////////////////////
	private String name;
	private int id;
	//////////////////////////////////////////////////////////////////////////////////////////////////
	//constructor
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public Item(int Id)
	{
		id = Id;
		if(Id == 0)
		{
			name = "Health potion";
		}
		else if(Id == 1)
		{
			name = "Speed potion";
		}
		else if(Id == 2)
		{
			name = "defense potion";
		}
		else if(Id == 3)
		{
			name = "Attack potion";
		}
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////
	//getters
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public String getItem()
	{
		return name;
	}
	public int getID()
	{
		return id;
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////
	//testing if items are equal
	//////////////////////////////////////////////////////////////////////////////////////////////////
	public boolean equals(int i)
	{
		if(id == i)
			return true;
		else 
			return false;
	}
}